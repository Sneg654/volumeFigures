package com.epam.figure;

public class Pyramid {
    private double height;
    private double squareBase;

    public Pyramid(double h, double sBase) {
        this.height = h;
        this.squareBase = sBase;
    }

    public double getHeight() {
        return height;
    }

    public double getSquareBase() {
        return squareBase;
    }
}
