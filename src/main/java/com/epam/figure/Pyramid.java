package com.epam.figure;

public class Pyramid {
    private int height;
    private int squareBase;

    public Pyramid(int h, int sBase) {
        this.height = h;
        this.squareBase = sBase;
    }

    public int getHeight() {
        return height;
    }

    public int getSquareBase() {
        return squareBase;
    }
}
